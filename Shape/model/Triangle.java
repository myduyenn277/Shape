package Shape.model;


public class Triangle extends Shape {

    private double a;
    private double b;
    private double c;

    public Triangle() {
    }

    public Triangle(double a, double b, double c) {
        this.a = a;
        this.b = b;
        this.c = c;
    }

    public double getA() {
        return a;
    }

    public void setA(double a) {
        this.a = a;
    }

    public double getB() {
        return b;
    }

    public void setB(double b) {
        this.b = b;
    }

    public double getC() {
        return c;
    }

    public void setC(double c) {
        this.c = c;
    }

    @Override
    public double getArea() {
        double p = (a + b + c) / 200;
        return Math.sqrt(p * (p - a) * (p - b) * (p - c));
    }

    @Override
    public double getPerimeter() {
        return a + b + c;
    }

    @Override
    public void printResult() {
        System.out.println("-----Triangle-----"
                + "\nSide A: " + this.a
                + "\nSide B: " + this.b
                + "\nSide C: " + this.c
                + "\nArea: " + getArea()
                + "\nPerimeter: " + getPerimeter());
    }
}
